import math


def task_1():
    print('Напишите программу, которая принимает на вход цифру,\n'
          'обозначающую день недели, и проверяет, является ли этот день выходным.\n')

    day = int(input('Введите день недели числом: '))

    if day == 6 or day == 7:
        print('Да')
    elif 1 <= day <= 5:
        print('Нет')
    else:
        print('Нет такого дня недели')


