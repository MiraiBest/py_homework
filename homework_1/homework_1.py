def task_3():
    print('Напишите программу, которая принимает на вход координаты точки (X и Y), \n'
          'причём X ≠ 0 и Y ≠ 0 и выдаёт номер четверти плоскости, \n'
          'в которой находится эта точка (или на какой оси она находится).\n')

    x, y = int(input('Введите координату X: ')), int(input('Введите координату Y: '))

    if x > 0 and y > 0:
        print('Четверть I')
    elif x < 0 and y > 0:
        print('Четверть II')
    elif x < 0 and y < 0:
        print('Четверть III')
    elif x > 0 and y < 0:
        print('Четверть IV')
    else:
        print('Точка лежит на оси')


def task_4():
    print('Напишите программу, которая по заданному номеру четверти, \n'
          'показывает диапазон возможных координат точек в этой четверти (x и y).\n')

    quarter = int(input('Ввведите номер четверти: '))

    if quarter == 1:
        print('X > 0; Y > 0;')
    elif quarter == 2:
        print('X < 0; Y > 0;')
    elif quarter == 3:
        print('X < 0; Y < 0;')
    elif quarter == 4:
        print('X > 0; Y < 0;')
    else:
        print('Нет такой четверти!')


